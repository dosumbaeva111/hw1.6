package com.company;

public class GameEntity {
    private double health;
    private int damage;
    private String superAbillity;

    public double getHealth() {
        return health;
    }

    public void setHealth(double health) {
        this.health = health;
    }

    public int getDamage() {
        return damage;
    }

    public void setDamage(int damage) {
        this.damage = damage;
    }

    public String getSuperAbillity() {
        return superAbillity;
    }

    public void setSuperAbillity(String superAbillity) {
        this.superAbillity = superAbillity;
    }
}
